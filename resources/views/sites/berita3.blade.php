                     <p class="excert">
                        Sedikitnya 21 Kelompok Usaha Bersama (KUBE) di Kabupaten Probolinggo mengikuti assistensi KUBE berkelanjutan program PFM (Penanganan Fakir Miskin) Wilayah III Kementerian Sosial (Kemensos) Republik Indonesia di Balai Desa Asembagus Kecamatan Kraksaan, Jum’at (6/9/2019).<p>
                        Kegiatan yang dibuka oleh Pelaksana Tugas (Plt) Asisten Administrasi Pemerintahan dan Kesra Kabupaten Probolinggo Moh. Happy ini diikuti oleh 115 orang terdiri dari OPD (Organisasi Perangkat Daerah) terkait, perwakilan kecamatan, kepala puskesmas dan TKSK se-Kabupaten Probolinggo.</p>
                        <p>
                        Ke-21 KUBE tersebut diantaranya KUBE Keluarga Harapan Asembagus, KUBE Karya Asembagus, KUBE Sumber Asembagus, KUBE Asembagus Kue Abadi, KUBE Sekar Arum Asembagus, KUBE Asembagus Maju Bersama, KUBE Mekar Jaya Asembagus, KUBE Asembagus Sumber Rejeki, KUBE Asembagus Makmur, KUBE Sumber Rejeki Asembakor, KUBE Salamah, KUBE Hindun, KUBE Khadijah, KUBE Sumber Usaha, KUBE Sumber Rejeki, KUBE Lancar Jaya, KUBE Berkah, KUBE U Hidayah, KUBE SP3 Mandiri dan KUBE SP4 Mandiri. KUBE ini tersebar di Kecamatan Kraksaan dan Paiton.</p>
                        <p class="excert">
                            Assistensi KUBE Berkelanjutan Program PFK Wilayah III ini dihadiri oleh Kepala Dinas Sosial (Dinsos) Kabupaten Probolinggo Achmad Arif, Kasubdit Potensi Dunia Usaha Direktorat Pemberdayaan Sosial Perorangan, Keluarga dan Kelembagaan Masyarakat Kementerian Sosial RI Lia Latifah Ningrum, Community & Human Resources Manager PT POMI Paiton Bambang Jiwantoro, Camat Kraksaan Muhamad Ridwan dan jajaran Forkopimka Kraksaan serta Kepala Desa Asembagus Ali Ibang Fansuri.</p>
                        <div class="quote-wrapper">
                        <div class="quotes">
                            “Kami ingin memberikan apresiasi kepada KUBE ini karena kalau Kementerian Sosial melakukan pembinaan terhadap KUBE tidak bisa sendiri. Tetapi lebih banyak bagaimana memotivasi masyarakat tidak mampu itu untuk lebih mandiri dan lebih banyak memberikan motivasi. Kemudian salah satunya juga memberikan dukungan bantuan sosial 
                            untuk usaha dan yang sudah kita berikan per kelompok Rp 25 juta untuk 10 orang,”ungkapnya.
                        </div>
                     </div>
                     <p>
                        Lia mengaku bahwa pihaknya mencoba bekerja sama dengan CSR supaya bisa memberikan penguatan-penguatan seperti kapasitas, membantu literasi keuangan dan memanage supaya dengan uang sekian bisa semakin maju. Selama ini KUBE menghitungnya sangat sederhana.</p>
                     <p class="excert">
                        “Harapan kita dengan adanya dukungan CSR bisa bersinergi kemudian membantu usaha kelompok usaha ini menjadi lebih maju. Kita juga tidak berharap sangat tinggi majunya, kalau bisa tinggi bagus. Minimal ada peningkatan karena Kementerian Sosial tidak bekerja sendiri. Kita juga sudah kerja sama dengan instansi terkait. Seperti peternakan, pertanian atau koperasi, tetapi masih sangat terbatas.<p>
                        “Penonaktifan peserta PBI-JK ini disebabkan beberapa hal. Yakni, tidak masuk dalam DTKS (Data Terpadu Kesejahteraan Sosial), tidak ada nomor NIK dan KK, tidak pernah melakukan layanan kesehatan, terdaftar ganda dengan segmen lain BPJS Ketenagakerjaan dan mandiri serta ganda kepesertaan, meninggal dunia, mampu dan tidak ditemukan,” katanya.</p>
                    <p class="excert">
                        Menurut Arif, ke-21 KUBE di Kabupaten Probolinggo ini sudah mendapatkan bantuan dari Kemensos RI pada tahun 2018 dan diharapkan bantuan tersebut dapat bermanfaat. Terlebih Kemensos menyampaikan apresiasi yang tinggi terhadap Pemerintah Kabupaten Probolinggo karena KUBE yang ada sudah sangat baik.</p>  
